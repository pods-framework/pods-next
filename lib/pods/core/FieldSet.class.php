<?php
namespace pods\core {


	class FieldSet extends \ArrayObject implements FieldSetItem {

		/**
		 * @param FieldSetItem $fieldSet
		 *
		 */
		public function addFieldSet( FieldSetItem $fieldSet ) {
			$this->append( $fieldSet );
		}

		public function getIterator() // TODO: maybe return \RecursiveArrayIterator instead of \ArrayIterator
		{
			parent::getIterator(); // TODO: Change the autogenerated stub
		}


		/**
		 * @param Field $field
		 *
		 * @return int number of total field after adding a field
		 */
		public function addField( Field $field ) {
			return array_push( $this->fields, $field );
		}

		/**
		 * @return int number of fields in the field set (does not include nested Fields)
		 */
		public function countFields() {
			$numberOfFields = count( $this->fields );

			return $numberOfFields;
		}


	}
}